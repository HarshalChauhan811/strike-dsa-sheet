#include <iostream>
#include <string>
using namespace std;

int main() {

    // Integer type
    int age = 25;

    // Decimal value
    double height = 5.9;

    // Single character
    char grade = 'A';

    // Boolean value
    bool isStudent = true;

    // String type
    string name = "Rohit";

    // Printing all values
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Grade: " << grade << endl;
    cout << "Student: " << isStudent << endl;

    return 0;
}